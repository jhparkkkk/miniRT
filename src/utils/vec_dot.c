#include "mini_rt.h"

double   vec_dot(t_vec3 v1, t_vec3 v2)
{
    double   x;
    double   y;
    double   z;

    x = v1.x * v2.x;
    y = v1.y * v2.y;
    z = v1.z * v2.z;
    return (x + y + z);
}