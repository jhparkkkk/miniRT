#include "mini_rt.h"

double	vec_len(t_vec3 v)
{
	t_vec3	res;

	res.x = v.x * v.x;
	res.y = v.y * v.y;
	res.z = v.z * v.z;
	return (sqrt(res.x + res.y + res.z));
}
